public class time {
    public static void main(String[] args) {
        System.out.println("time:");

        for(int h=1;h<=1;h++){

            System.out.println(h);

            for(int m=1;m<=60;m++){

                System.out.println(m+" minute ");

                for(int s=1;s<=60;s++){

                    System.out.println(s+" second ");
                }
                System.out.println();

            }
        }
    }
}

